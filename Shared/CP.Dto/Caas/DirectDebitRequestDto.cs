            “externalTrxId”: “12345678901234567890123456789012”,

            “subscriberId”: “94771234567”,

            “paymentInstrumentName”: “MobileAccount”,

            “accountId”: “123456”,

            “currency”: “LKR”,

            “amount”: “100”
        } */

    }
}
